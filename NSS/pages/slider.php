<p style="text-align: justify;"  > <h4><b>National Integration Camp (NIC) </b> </h4></p> 
<p style="text-align: justify;"  > <h5><b>The National Integration Camp (NIC) is organized every year and the duration of each camp is of 7 days with day-night boarding & lodging. These camps are held in different parts of the country. Each camp involves 200 NSS volunteers to undertake the scheduled activities.</b> </h5></p> 

